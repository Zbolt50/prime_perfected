// Rotate OLED
oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (!is_keyboard_master()) {
        return OLED_ROTATION_270;  // flips the display 180 degrees if offhand
    }
    else {
        return OLED_ROTATION_270;
    }
    return rotation;
}

static void print_status_left(void) {
        oled_set_cursor(0,1);
        oled_write("LAYER", false);
        switch (get_highest_layer(layer_state))
        {
            case 0 :
                oled_write("BASE", false);
                break;
            case 1 :
                oled_write("FUNC", false);
                break;
            case 2 :
                oled_write("SYM ", false);
                break;
            case 3:
                oled_write("NUM ", false);
        }
        oled_set_cursor(0,6);
        oled_write_raw_P(siva_logo,sizeof(siva_logo));
        oled_set_cursor(0,12);
        os_variant_t host = detected_host_os();
            switch (host)
            {
            case OS_MACOS:
                oled_write_raw_P(mac_ios_logo, sizeof(mac_ios_logo));
            case OS_IOS:
                oled_write_raw_P(mac_ios_logo, sizeof(mac_ios_logo));
                break;
            case OS_WINDOWS:
                oled_write("WIN", false);
                break;
            case OS_LINUX:
                oled_write_raw_P(linux_logo, sizeof(linux_logo));
                break;
            case OS_UNSURE:
                oled_write("OS?", false);
                break;
            }
        //oled_scroll_left();  // Turns on scrolling

}
static void print_status_right(void) {
   oled_set_cursor(0,6);
   render_animation();

}

bool oled_task_user() {
    if (is_keyboard_master())
    {
       print_status_left();
    }
    else
    {
       print_status_right();
    }
    return false;
}

void oled_display_shutdown_message(bool bootloader) {
    oled_clear();
    oled_set_cursor(0, 4); // Centered for better visibility
    if (bootloader) {
        oled_write_P(PSTR("Awaiting Firmware..."), false);
    } else {
        oled_write_P(PSTR("Rebooting..."), false);
    }
    oled_render();
}

bool shutdown_user(bool jump_to_bootloader) {
    oled_display_shutdown_message(jump_to_bootloader);
    wait_ms(1000); // Give some time for the message to be seen
    reset_keyboard(); // Perform actual reboot or bootloader jump
    return true; // Indicate shutdown completed
}
